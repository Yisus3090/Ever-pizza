function PrecioEspecial(medida) {
    var precioSpan = document.getElementById("especial");
    
    switch (medida) {
      case "personal":
        precioSpan.textContent = "$25.000";
        break;
      case "small":
        precioSpan.textContent = "$30.000";
        break;
      case "medium":
        precioSpan.textContent = "$35.000";
        break;
      case "large":
        precioSpan.textContent = "$40.000";
        break;
      default:
        break;
    }
  }

